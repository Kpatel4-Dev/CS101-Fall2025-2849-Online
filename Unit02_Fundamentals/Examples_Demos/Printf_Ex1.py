#printing floating point number ïƒ  examples

number = 12345.67891213

print(f'{number}') #print the number as it is

print(f'{number:.2f}') #print the number to 2 decimal places

print(f'{number:10.2f}') #print the number to 2 decimal places, 10 spaces wide

print(f'{number:<10.2f}') #print the number to 2 decimal places, 10 spaces wide and left justified

print(f'{number:10,.2f}') #print the number to 2 decimal places, 10 spaces wide and comma seperated

print(f'My number is {number:,.3f}!') #print the number to 3 decimal places, comma seperated

